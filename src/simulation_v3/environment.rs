/// Environmental State
///
/// Represents the state of the environment at a given location.
/// The environment consists of energy fields, matter density, and vibrational fields.
#[derive(Debug, Clone)]
pub struct EnvironmentalState {
    /// Energy fields at different locations (using string keys)
    pub energy_fields: HashMap<String, EnergyField>,

    /// Matter density at different locations (using string keys)
    pub matter_density: HashMap<String, Float>,

    /// Vibrational field at different locations (using string keys)
    pub vibrational_fields: HashMap<String, VibrationalField>,

    /// Collective fields (for collective entities)
    pub collective_fields: HashMap<EntityId, CollectiveField>,

    /// Environmental coherence (0.0 to 1.0)
    pub environmental_coherence: Float,

    /// Environmental resonance frequency
    pub environmental_resonance: Float,
}

/// Energy Field
///
/// Represents an energy field at a specific location.
#[derive(Debug, Clone)]
pub struct EnergyField {
    /// Field location
    pub location: Coordinate3D,

    /// Energy intensity (0.0 to 1.0)
    pub intensity: Float,

    /// Field type
    pub field_type: EnergyFieldType,

    /// Field coherence (0.0 to 1.0)
    pub coherence: Float,
}

/// Type of energy field
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum EnergyFieldType {
    /// Intelligent Infinity energy (unconditional love)
    IntelligentInfinity,

    /// Light/Love energy (creative energy)
    LightLove,

    /// Logos energy (structuring energy)
    Logos,

    /// Archetypical energy (pattern energy)
    Archetypical,

    /// Catalyst energy (transformative energy)
    Catalyst,
}

/// Vibrational Field
///
/// Represents the vibrational state at a specific location.
#[derive(Debug, Clone)]
pub struct VibrationalField {
    /// Field location
    pub location: Coordinate3D,

    /// Vibrational frequency (0.0 to 1.0)
    pub frequency: Float,

    /// Vibrational coherence (0.0 to 1.0)
    pub coherence: Float,

    /// Vibrational phase (0.0 to 2π)
    pub phase: Float,
}

/// Collective Field
///
/// Represents the collective field generated by a collective entity.
#[derive(Debug, Clone)]
pub struct CollectiveField {
    /// Collective entity ID
    pub collective_id: EntityId,

    /// Field strength (0.0 to 1.0)
    pub strength: Float,

    /// Field coherence (0.0 to 1.0)
    pub coherence: Float,

    /// Field influence radius
    pub influence_radius: Float,

    /// Member entities contributing to this field
    pub contributing_entities: Vec<EntityId>,
}

/// Environmental Effect
///
/// Represents the effect of environment on an entity (or entity on environment).
#[derive(Debug, Clone)]
pub struct EnvironmentalEffect {
    /// Energy field effect
    pub energy_effect: Float,

    /// Matter density effect
    pub matter_effect: Float,

    /// Vibrational field effect
    pub vibrational_effect: Float,

    /// Overall magnitude of effect (0.0 to 1.0)
    pub magnitude: Float,

    /// Effect type (influence from environment or influence to environment)
    pub effect_type: EnvironmentalEffectType,
}

/// Type of environmental effect
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum EnvironmentalEffectType {
    /// Environment affecting entity
    EnvironmentToEntity,

    /// Entity affecting environment
    EntityToEnvironment,

    /// Bidirectional influence
    Bidirectional,
}

