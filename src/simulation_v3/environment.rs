#[cfg(test)]
mod tests {
    use super::*;
    use crate::entity_layer7::layer7::{IndividualSpectrumConfiguration, SubSubLogos};
    use crate::foundation::{BlueRealm, GreenRealm, IndigoRealm, VioletRealm};
    use crate::spectrum::{OrangeRealm, RedRealm, SpectrumRatio, SpectrumSide, YellowRealm};

    /// Create a test entity
    fn create_test_entity(id: &str) -> SubSubLogos {
        let violet = VioletRealm::new();
        let indigo = IndigoRealm::new();
        let blue = BlueRealm::new();
        let green = GreenRealm::new();
        let yellow = YellowRealm::new(green.clone());
        let orange = OrangeRealm::new(yellow.clone());
        let red = RedRealm::new(orange.clone());

        let ratio = SpectrumRatio::new(1.5, SpectrumSide::SpaceTime);
        let spectrum_config = IndividualSpectrumConfiguration::new(ratio);

        SubSubLogos::new(
            EntityId::new(id.to_string()),
            crate::entity_layer7::layer7::EntityType::Individual,
            None,   // parent_id
            vec![], // composition
            None,   // environment_id
            violet,
            indigo,
            blue,
            green,
            yellow,
            orange,
            red,
            spectrum_config,
        )
    }

    #[test]
    fn test_environmental_interaction_manager_creation() {
        let manager = EnvironmentalInteractionManager::new();
        assert_eq!(manager.simulation_time, 0);
        assert_eq!(manager.statistics.total_interactions, 0);
    }

    #[test]
    fn test_initialize_environment() {
        let mut manager = EnvironmentalInteractionManager::new();

        let entity1 = create_test_entity("entity-1");
        let entity2 = create_test_entity("entity-2");

        let mut entities = HashMap::new();
        entities.insert(entity1.entity_id.clone(), entity1);
        entities.insert(entity2.entity_id.clone(), entity2);

        manager.initialize_environment(&entities);

        // Should have energy fields and vibrational fields
        assert!(manager.environment.energy_fields.len() > 0);
        assert!(manager.environment.vibrational_fields.len() > 0);
    }

    #[test]
    fn test_interact_with_environment() {
        let mut manager = EnvironmentalInteractionManager::new();

        let entity = create_test_entity("entity-1");
        let entity_id = entity.entity_id.clone();

        let mut entities = HashMap::new();
        entities.insert(entity_id.clone(), entity.clone());

        manager.initialize_environment(&entities);

        let effect = manager.interact_with_environment(&entity_id, &entity);

        // Should have some effect
        assert!(effect.magnitude >= 0.0);
        assert_eq!(manager.statistics.total_interactions, 1);
    }
}
