/// Archetype indices for Mind (A1-A7)
const MIND_START: usize = 0;
const MIND_END: usize = 7;

/// Archetype indices for Body (A8-A14)
const BODY_START: usize = 7;
const BODY_END: usize = 14;

/// Archetype indices for Spirit (A15-A21)
const SPIRIT_START: usize = 14;
const SPIRIT_END: usize = 21;

/// Archetype index for Choice (A22)
const CHOICE_INDEX: usize = 21;

