//! Observation Layer — Bridge between HoloSim simulation state and game-engine-consumable data.
//!
//! This module translates internal simulation state (archetype activations, spectrum position,
//! density, coherence) into structured observations that game engines (Bevy, Godot) can consume
//! directly without understanding HoloSim internals.
//!
//! # Architecture
//!
//! ```text
//! HoloSim Internals          Observation Layer           Game Engine
//! ┌─────────────────┐       ┌─────────────────┐       ┌─────────────────┐
//! │ ArchetypeBasis  │──────▶│ PhysicalObs     │──────▶│ Position, Mass  │
//! │ SpectrumConfig  │──────▶│ BehavioralObs   │──────▶│ Needs, Mood     │
//! │ Density         │──────▶│ EnvironmentalObs│──────▶│ Terrain, Weather│
//! │ FreeWill        │──────▶│ Events          │──────▶│ Game Events     │
//! └─────────────────┘       └─────────────────┘       └─────────────────┘
//! ```
//!
//! # Mapping Logic
//!
//! - **Archetype → Needs**: A1-A7 (Mind) → exploration; A8-A14 (Body) → hunger, rest;
//!   A15-A21 (Spirit) → social, growth; A22 (Choice) → proportional influence on all.
//! - **Spectrum → Environment**: spectrum_position maps to terrain type (quantum → cosmic).
//! - **Density → Capabilities**: Higher density entities have higher health ceilings.
//! - **Mood**: Derived from archetype interference pattern (-1.0 distressed to 1.0 harmonious).

