    /// Compute and observe the holographic field using the GPU/CPU compute engine.
    ///
    /// Calls `compute_connections` and `compute_resonance` on the engine, converts
    /// results into game-consumable observation types, and stores them.
    ///
    /// # Arguments
    /// * `engine` — The compute engine (GPU or CPU backend)
    /// * `entities` — Entity data prepared for GPU upload
    pub fn observe_gpu_field(
        &mut self,
        engine: &crate::gpu::compute_engine::ComputeEngine,
        entities: &[crate::gpu::entity_upload::EntityGPUData],
    ) {
        use crate::gpu::connection_kernel::ConnectionWeights;
        use crate::gpu::resonance_kernel::ResonanceWeights;

        let num_entities = entities.len();
        if num_entities == 0 {
            self.gpu_observation = Some(GpuFieldObservation {
                backend: engine.backend().to_string(),
                num_entities: 0,
                num_connections: 0,
                num_resonance_pairs: 0,
                avg_connection_strength: 0.0,
                avg_resonance_score: 0.0,
                compute_time_ms: 0.0,
                connections: Vec::new(),
                resonance_pairs: Vec::new(),
            });
            return;
        }

        let start = std::time::Instant::now();

        let connections = engine.compute_connections(entities, &ConnectionWeights::default());
        let resonance = engine.compute_resonance(entities, &ResonanceWeights::default());

        let compute_time_ms = start.elapsed().as_secs_f64();

        let entity_id =
            |idx: usize| -> String { format!("0x{:08x}", entities[idx].entity_id_hash) };

        let connection_observations: Vec<GpuConnectionObservation> = connections
            .iter()
            .enumerate()
            .map(|(flat_idx, c)| {
                let row = flat_idx / num_entities;
                let col = flat_idx % num_entities;
                GpuConnectionObservation {
                    entity_a_id: entity_id(row),
                    entity_b_id: entity_id(col),
                    strength: c.strength,
                    archetype_similarity: c.archetype_similarity,
                    spectrum_similarity: c.spectrum_similarity,
                    blueprint_alignment: c.blueprint_alignment,
                    resonance_match: c.resonance_match,
                    phase_coherence: c.phase_coherence,
                    karmic_correlation: c.karmic_correlation,
                    connection_type: c.connection_type,
                }
            })
            .collect();

        let resonance_observations: Vec<GpuResonanceObservation> = resonance
            .iter()
            .enumerate()
            .map(|(flat_idx, r)| {
                let row = flat_idx / num_entities;
                let col = flat_idx % num_entities;
                GpuResonanceObservation {
                    entity_a_id: entity_id(row),
                    entity_b_id: entity_id(col),
                    resonance_score: r.resonance_score,
                    spectrum_resonance: r.spectrum_resonance,
                    holographic_resonance: r.holographic_resonance,
                    polarity_resonance: r.polarity_resonance,
                    resonance_type: r.resonance_type,
                }
            })
            .collect();

        let num_connections = connection_observations.len();
        let num_resonance_pairs = resonance_observations.len();

        let avg_connection_strength = if num_connections > 0 {
            connection_observations
                .iter()
                .map(|c| c.strength)
                .sum::<f32>()
                / num_connections as f32
        } else {
            0.0
        };

        let avg_resonance_score = if num_resonance_pairs > 0 {
            resonance_observations
                .iter()
                .map(|r| r.resonance_score)
                .sum::<f32>()
                / num_resonance_pairs as f32
        } else {
            0.0
        };

        self.gpu_observation = Some(GpuFieldObservation {
            backend: engine.backend().to_string(),
            num_entities,
            num_connections,
            num_resonance_pairs,
            avg_connection_strength,
            avg_resonance_score,
            compute_time_ms,
            connections: connection_observations,
            resonance_pairs: resonance_observations,
        });
    }

    /// Get the most recent GPU field observation, if one has been computed.
    pub fn get_gpu_observation(&self) -> Option<&GpuFieldObservation> {
        self.gpu_observation.as_ref()
    }

    // ========================================================================
    // Mapping: Archetype → Needs
    // ========================================================================

    /// Map archetype activation profile to behavioral needs.
    ///
    /// Mapping:
    /// - A1-A7 (Mind) → exploration need (curiosity from cognitive activation)
    /// - A8-A14 (Body) → hunger and rest needs (physical maintenance)
    /// - A15-A21 (Spirit) → social and growth needs
    /// - A22 (Choice) → proportional influence on all needs
    ///
    /// Formula: `need = avg(archetype_coeffs[range]) * (1.0 - satisfaction_base)`
    /// where satisfaction_base is derived from coherence (higher coherence = more satisfied).
    fn compute_needs(archetype_profile: &[f64; 22], coherence: f64) -> [f64; 5] {
        let satisfaction_base = coherence.clamp(0.0, 1.0);

        let mind_sum: f64 = archetype_profile[MIND_START..MIND_END].iter().sum();
        let mind_avg = mind_sum / (MIND_END - MIND_START) as f64;

        let hunger_sum: f64 = archetype_profile[BODY_START..11].iter().sum();
        let hunger_avg = hunger_sum / (11 - BODY_START) as f64;
        let rest_sum: f64 = archetype_profile[11..BODY_END].iter().sum();
        let rest_avg = rest_sum / (BODY_END - 11) as f64;

        let social_sum: f64 = archetype_profile[SPIRIT_START..18].iter().sum();
        let social_avg = social_sum / (18 - SPIRIT_START) as f64;
        let growth_sum: f64 = archetype_profile[18..SPIRIT_END].iter().sum();
        let growth_avg = growth_sum / (SPIRIT_END - 18) as f64;

        let choice = archetype_profile[CHOICE_INDEX].clamp(0.0, 1.0);
        let choice_influence = 1.0 + choice * 0.5;

        let mut needs = [
            (hunger_avg * (1.0 - satisfaction_base) * choice_influence).clamp(0.0, 1.0),
            (social_avg * (1.0 - satisfaction_base) * choice_influence).clamp(0.0, 1.0),
            (rest_avg * (1.0 - satisfaction_base) * choice_influence).clamp(0.0, 1.0),
            (mind_avg * (1.0 - satisfaction_base) * choice_influence).clamp(0.0, 1.0),
            (growth_avg * (1.0 - satisfaction_base) * choice_influence).clamp(0.0, 1.0),
        ];

        let sum: f64 = needs.iter().sum();
        if sum > 5.0 {
            let scale = 5.0 / sum;
            for n in &mut needs {
                *n *= scale;
            }
        }

        needs
    }

    // ========================================================================
    // Mapping: Mood calculation from archetype interference
    // ========================================================================

    /// Calculate mood from archetype interference pattern.
    ///
    /// mood = (positive_archetype_sum - negative_archetype_sum) / total_activation
    /// Range: -1.0 (distressed) to 1.0 (harmonious)
    fn compute_mood(archetype_profile: &[f64; 22]) -> f64 {
        let positive_sum: f64 = archetype_profile[SPIRIT_START..SPIRIT_END].iter().sum();

        let body_sum: f64 = archetype_profile[BODY_START..BODY_END].iter().sum();
        let body_count = (BODY_END - BODY_START) as f64;
        let body_mean = if body_count > 0.0 {
            body_sum / body_count
        } else {
            0.0
        };
        let body_variance: f64 = archetype_profile[BODY_START..BODY_END]
            .iter()
            .map(|&a| (a - body_mean).powi(2))
            .sum();

        let mind_sum: f64 = archetype_profile[MIND_START..MIND_END].iter().sum();

        let total: f64 = archetype_profile.iter().map(|&a| a.abs()).sum();
        if total < 1e-10 {
            return 0.0;
        }

        let distress_signal = if mind_sum > body_sum {
            0.0
        } else {
            body_variance / body_count
        };

        let mood = (positive_sum - distress_signal) / total;
        mood.clamp(-1.0, 1.0)
    }

