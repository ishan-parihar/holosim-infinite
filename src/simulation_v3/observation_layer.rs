/// Terrain classification based on holographic spectrum position.
/// Maps the space/time ↔ time/space continuum to game-world terrain types.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum TerrainType {
    /// Quantum realm — spectrum_position < 0.3, high energy, subatomic
    Quantum,
    /// Biological realm — spectrum_position 0.3–0.5, organic structures
    Biological,
    /// Planetary realm — spectrum_position 0.5–0.7, world-scale
    Planetary,
    /// Stellar realm — spectrum_position 0.7–0.9, star-scale
    Stellar,
    /// Cosmic realm — spectrum_position > 0.9, galactic/universal
    Cosmic,
}

impl std::fmt::Display for TerrainType {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            TerrainType::Quantum => write!(f, "quantum"),
            TerrainType::Biological => write!(f, "biological"),
            TerrainType::Planetary => write!(f, "planetary"),
            TerrainType::Stellar => write!(f, "stellar"),
            TerrainType::Cosmic => write!(f, "cosmic"),
        }
    }
}

// ============================================================================
// Event types from free will choices and state transitions
// ============================================================================

/// Types of simulation events generated by entity state changes.
#[derive(Debug, Clone, PartialEq, Eq, Hash)]
pub enum EventType {
    /// Entity changed density level
    DensityTransition { from_density: u8, to_density: u8 },
    /// Entity made a significant choice (high A22 activation)
    SignificantChoice,
    /// Entity experienced archetype interference spike
    ArchetypicalEvent,
    /// Entity entered a new terrain region
    TerrainChange { terrain: TerrainType },
    /// Entity coherence dropped critically
    CoherenceDrop,
    /// Entity coherence reached peak
    CoherencePeak,
}

impl std::fmt::Display for EventType {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            EventType::DensityTransition {
                from_density,
                to_density,
            } => write!(f, "DensityTransition({}→{})", from_density, to_density),
            EventType::SignificantChoice => write!(f, "SignificantChoice"),
            EventType::ArchetypicalEvent => write!(f, "ArchetypicalEvent"),
            EventType::TerrainChange { terrain } => write!(f, "TerrainChange({})", terrain),
            EventType::CoherenceDrop => write!(f, "CoherenceDrop"),
            EventType::CoherencePeak => write!(f, "CoherencePeak"),
        }
    }
}

