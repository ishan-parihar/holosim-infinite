#[derive(Debug, Clone)]
pub struct PhysicalObservation {
    pub entity_id: u64,
    pub position: [f64; 3],
    pub velocity: [f64; 3],
    pub mass: f64,
    pub temperature: f64,
    pub charge: f64,
    pub health: f64,
    pub energy_level: f64,
}

/// Behavioral outputs derived from archetype interference patterns.
/// Game engines consume this for AI, needs systems, and personality.
#[derive(Debug, Clone)]
pub struct BehavioralObservation {
    pub entity_id: u64,
    /// [Hunger, Social, Rest, Exploration, Growth] — each 0.0–1.0
    pub needs: [f64; 5],
    /// -1.0 (distressed) to 1.0 (harmonious), from archetype interference
    pub mood: f64,
    /// Emergent goal from free will + archetype pull
    pub current_goal: Option<String>,
    /// Direct 22-archetype activation profile
    pub personality_profile: [f64; 22],
    /// 0.0–1.0, behavioral consistency
    pub stability: f64,
    /// 0.0–1.0, behavioral novelty/unexpectedness
    pub novelty: f64,
}

