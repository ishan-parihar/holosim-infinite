    /// Map spectrum position to terrain type.
    ///
    /// spectrum_position < 0.3 → Quantum (high energy, subatomic)
    /// spectrum_position 0.3–0.5 → Biological (organic)
    /// spectrum_position 0.5–0.7 → Planetary (world-scale)
    /// spectrum_position 0.7–0.9 → Stellar (star-scale)
    /// spectrum_position > 0.9 → Cosmic (galactic/universal)
    fn spectrum_to_terrain(spectrum_position: f64) -> TerrainType {
        if spectrum_position < 0.3 {
            TerrainType::Quantum
        } else if spectrum_position < 0.5 {
            TerrainType::Biological
        } else if spectrum_position < 0.7 {
            TerrainType::Planetary
        } else if spectrum_position < 0.9 {
            TerrainType::Stellar
        } else {
            TerrainType::Cosmic
        }
    }

    // ========================================================================
    // Mapping: Density → Health capability
